// Diferencia entre metodos y funciones 

const numero1 = 20; 
const numero2 = '20';

console.log(parseInt(numero2)); // paresInt es una funcion
console.log(numero1.toString()); // toString es un metodo. 

// La diferencia radica en que las funciones son independientes y los metodos dependen de otras cosas para funcionar, como variables, arreglos, clases, objetos, etc. 